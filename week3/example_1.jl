using JuMP
using Clp
m = Model(Clp.Optimizer)
@variable(m, x11 ≥ 0)
@variable(m, x12 ≥ 0)
@variable(m, x21  ≥ 0)
@variable(m, x22 ≥ 0)
@variable(m, x31 ≥ 0)
@variable(m, x32 ≥ 0)
@objective(m, Min, 80x11 + 100x21 + 102x31 + 251x12 + 108x22 + 68x32)
@constraint(m, x11 + x12 ≤ 1000)
@constraint(m, x21 + x22 ≤ 1500)
@constraint(m, x31 + x32 ≤ 1200)
@constraint(m, x11 + x21 + x31 == 2300)
@constraint(m, x12 + x22 + x32 == 1400)
print(m)
optimize!(m)
status = termination_status(m)
println("Solution status: ", status)
println("Objective value: ", objective_value(m))
println("x11: ", value(x11))
println("x12: ", value(x12))
println("x21 ", value(x21))
println("x22 ", value(x22))
println("x31", value(x31))
println("x32 ", value(x32))